    /**
     * 类头注释
     *
     * @param compilationUnit
     */
    @Override
    public void addJavaFileComment(CompilationUnit compilationUnit) {
        // add no file level comments by default
        String template = createTemplate();
        if (compilationUnit instanceof TopLevelClass) {
            TopLevelClass tlc = (TopLevelClass) compilationUnit;
            tlc.addJavaDocLine(template);
        } else if (compilationUnit instanceof Interface) {
            Interface ifs = (Interface) compilationUnit;
            ifs.addJavaDocLine(template);
        } else if (compilationUnit instanceof TopLevelEnumeration) {
            TopLevelEnumeration tle = (TopLevelEnumeration) compilationUnit;
            tle.addJavaDocLine(template);
        }
    }

    private String createTemplate() {
        StringBuilder docLine = new StringBuilder();
        docLine.append("/**");
        docLine.append("\n");
        docLine.append("* @author ");
        docLine.append(systemPro.getProperty("user.name", ""));
        docLine.append("\n");
        docLine.append("*/");
        return docLine.toString();
    }

